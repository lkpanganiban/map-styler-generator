export function registerProjection(code: string, projString: string) {
  const epsgCode = code.replace(/^urn:ogc:def:crs:EPSG:{0,2}/i, 'EPSG:')
  try {
    proj4.defs(epsgCode, projString)
  } catch {
    console.warn(`Failed to register projection: ${epsgCode}`)
  }
}

